package com.september;

public class Star8 {
    public static void main(String[] args) {
        for (int i=1;i<=5;i++){
            for (int j=1;j<=5;j++){
                System.out.print("*");
            }
            System.out.println();
        }
        for (int k = 1; k<=5; k++){
            for (int l = 1; l<=5; l++){
                System.out.print("&");
            }
            System.out.println();
        }
        for (int a=1;a<=5;a++){
            for (int b=1;b<=5;b++){
                System.out.print("$");
            }
            System.out.println();
        }
        for (int i=1;i<=5;i++){
            for (int j=1;j<=5;j++){
                System.out.print("#");
            }
            System.out.println();
        }
        for (int i=1;i<=5;i++){
            for (int j=1;j<=5;j++){
                System.out.print("@");
            }
            System.out.println();
        }
    }
}
